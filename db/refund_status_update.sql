-- 更新支持退款的订单状态说明
-- 原来：只有SUCCESS(5)状态可以申请退款
-- 现在：CONFIRM(4)和SUCCESS(5)状态都可以申请退款

-- 对应状态：
-- 1: 待付款 (UNPAY) - 不能退款
-- 2: 待发货 (PADYED) - 不能退款
-- 3: 待收货 (CONSIGNMENT) - 不能退款
-- 4: 待评价 (CONFIRM) - 可以退款 ✓
-- 5: 交易成功 (SUCCESS) - 可以退款 ✓
-- 6: 交易失败 (CLOSE) - 不能退款

-- 前端修改：
-- order-detail.vue: 退款按钮显示条件从 status==4 修改为 status==4||status==5

-- 后端修改：
-- RefundServiceImpl.java: 退款状态验证从只允许SUCCESS修改为允许CONFIRM和SUCCESS

-- 说明：
-- 用户确认收货后（CONFIRM状态）就应该可以申请退款
-- 评价完成后（SUCCESS状态）仍然可以申请退款
-- 这符合正常的电商退款业务流程